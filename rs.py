# pySerial для RS-232:
import serial

# Настройка последовательного порта
ser = serial.Serial('COM3', 9600, timeout=1)

# Отправка данных
ser.write(b'Hello, RS-232!')

# Чтение данных
data = ser.read(10)
print(data)

# Закрытие порта
ser.close()
